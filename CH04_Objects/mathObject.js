// {## Math Object}

let number = 7.6;

console.log(Math.round(number)); // 8
console.log(Math.floor(number)); // 7 //Just Less than the number
console.log(Math.ceil(number)); //8 //Just Greater than the number

console.log(Math.trunc(number)); // 7 // Remove all the decimal digit

let random = Math.random();

console.log(Math.round(random * 1000000));
